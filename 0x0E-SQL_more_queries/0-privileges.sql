-- Displaying privileges for specific users on the local host.
-- This command retrieves the grants for 'user_0d_1' and 'user_0d_2' accounts.
-- It provides information about the permissions and privileges these users have within the database environment.
-- Useful for auditing and managing user access to the database.
SHOW GRANTS FOR 'user_0d_1'@'localhost';
SHOW GRANTS FOR 'user_0d_2'@'localhost';

